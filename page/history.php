                <h5 class="card-title" style="font-family: 'Kanit', sans-serif;"><i class="fa fa-globe"></i> วิธีการสร้างมหาพีระมิดแห่งกีซา</h5>
                <hr>

                <p style="margin-left: 5px; margin-right: 5px;">นับเป็นเรื่องที่น่าทึ่งมากๆ เพราะในตอนนั้นที่ยังไม่มีปั้นจั่น ไม่มีรถลาก การเคลื่อนย้ายหินหนักเป็นตันทำกันด้วยแรงคนล้วนๆ ใช้แรงงานลากเข็นไปบนแคร่ไม้ โดยมีการราดน้ำเพื่อช่วยลดแรงเสียดทาน การเคลื่อนย้ายวัตถุน้ำหนักมากๆ ด้วยวิธีนี้มีหลักฐานเป็นภาพแกะสลักนูนต่ำบนฝาผนังหิน แถมหินที่นำมาใช้สร้างพีระมิดนี้ก็ไม่ได้อยู่ในแถวนั้นเลยด้วย ต้องขนส่งมาจากเหมืองหินปูนที่อยู่ห่างออกไปนับร้อยไมล์ โดยล่องมาตามแม่น้ำไนล์ส่วนการลำเลียงหินขึ้นไปวางบนชั้นที่สูงขึ้นเรื่อยๆ จนถึงยอดนั้นยังเป็นปริศนา และมีหลากหลายทฤษฎีการสร้างมาก ได้แก่

1. แนวคิดแรกเริ่มเชื่อกันว่าชาวอียิปต์โบราณใช้วิธีสร้างทางลาดบริเวณด้านข้างของพีระมิด และชักลากหินขึ้นตามทางลาดที่ก่อสูงขึ้นเรื่อยๆ ตามความสูงของระดับการก่อสร้างจนถึงจุดสูงสุดยอด และเมื่อการก่อสร้างแล้วเสร็จจึงทำการรื้อทางลาดดังกล่าวออก คงเหลือไว้แต่พีระมิดที่สร้างเสร็จแล้ว

2. สร้างเป็นทางวนรอบพีระมิด หรือบางทีแต่ละชั้นของพีระมิดนั่นเองคือทางที่ใช้ชักลากหินขึ้นสู่ชั้นถัดไป ผ่านทางลาดขนาดเล็กที่สร้างขึ้นเพื่อเชื่อมต่อระหว่างแต่ละชั้น
