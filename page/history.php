                <p><u>ประวัติพีระมิดแห่งกีซา</u>
            <br>หน้าที่หลักของมันก็คือเป็นที่ฝังพระศพของกษัตริย์อียิปต์โบราณ หรือ ฟาโรห์  และเพื่อให้แน่ใจว่าองค์ฟาโรห์จะมีข้าวของเครื่องใช้ และทรัพย์สินเงินทองที่เพียงพอสำหรับโลกหน้าอย่างสุขสบาย  นอกจากนั้นยังรวมถึงอาหาร เครื่องเรือน เครื่องดนตรี อาวุธ อุปกรณ์ล่าสัตว์ ไปจนกระทั่งรถศึกเทียมม้าบางครั้งก็ยังรวมถึงการประหารชีวิตคน เพื่อไปทำหน้าที่ในการเฝ้าสมบัติของพระองค์สุสานในช่วงยุคแรกๆ จะยังไม่ใช่ทรงพีระมิดแต่จะแค่ขุดหลุมลงไปเป็นทรงสี่เหลี่ยม หรือวงรี แล้วค่อยฝังศพพร้อมกับข้าวของเครื่องใช้ลงไป กระทั่งการมาของการสร้างสุสานแบบ "มาสตาบา" (Mastaba) พีระมิดทรงสี่เหลี่ยมคางหมู ซึ่งจะสามารถสร้างห้องไว้ภายในได้มากขึ้น เก็บของได้มากขึ้น จากนั้นจึงเป็นต้นแบบให้เกิดการสร้างพีระมิดแบบขั้นบันไดในยุคต่อๆ มา พีระมิดแห่งกีซา
ซึ่งพีระมิดนี้จะเอาไว้เรียกกลุ่มพีระมิดของฟาโรห์ในยุคราชวงศ์ที่ 4 ซึ่งประกอบไปด้วยพีระมิดของฟาโรห์คูฟู (Khufu) คาเฟร (Khafre) และเมนคาอูเร (Menkaure) ซึ่งพีระมิดที่ใหญ่ที่สุด ที่เรียกกันว่ามหาพีระมิดแห่งกีซานั้นก็คือของฟาโรห์คูฟู
